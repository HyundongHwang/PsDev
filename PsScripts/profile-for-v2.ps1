function hhdkillwithchild
{
    [CmdletBinding()]
    param
    (
        [Parameter(Mandatory=$false, ValueFromPipeline=$true, ValueFromPipelinebyPropertyName=$true)]
        [System.String]
        $processName
    )

    $processList = ps "*$processName*"
    $processList

    Read-Host "kill process list ???"
    $processList | % { taskkill /PID $_.id /T /F }
}



